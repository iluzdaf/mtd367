// - Brings in Apple’s Foundation framework.
// - It gives you extra tools like:
//     - Dates and times
//     - Random numbers
//     - Advanced String manipulation
//     - Collections with more functionality
// - You might not always need it for simple examples,
//   but it’s widely used in real-world projects.
import Foundation

